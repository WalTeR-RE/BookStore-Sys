    private void SubmitDeleteActionPerformed(ActionEvent evt){
        if(BookID.getText().equals("")&&Average.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"You have to enter some values here");
            return;
        }
        DeleteBook.DeleteBookStatus stat;
        if(!BookID.getText().equals(""))
        {
            stat = DeleteBook.DeleteCurrentBook(currentuser.getInstance(),Integer.parseInt(BookID.getText()),0f);
        }
        else
        {
            System.out.println(Average.getText());
            stat = DeleteBook.DeleteCurrentBook(currentuser.getInstance(),0,Float.parseFloat(Average.getText()));
        }

        switch (stat) {
            case NOTFOUND -> {
                JOptionPane.showMessageDialog(null, "Not Found!");
                return;
            }
            case DBERROR -> {
                JOptionPane.showMessageDialog(null, "UnKnown Error\n");
                return;
            }
            case FAIL -> {
                JOptionPane.showMessageDialog(null, "Invalid Inputs");
                return;
            }
            case OK -> {
                JOptionPane.showMessageDialog(null, "Book Deleted Succefully");
                Loader.LoadBooks();
            }
        }
    }
